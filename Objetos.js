var busqueda= document.getElementById("buscar");
function cambiar_fondo_focus(){

    busqueda.style.background= "#BCF1F9";
}
function cambiar_fondo_blur() {
    // No focus = Changes the background color of input to red
    busqueda.style.background= "white";
  }
busqueda.addEventListener("focus",cambiar_fondo_focus);
busqueda.addEventListener("blur",cambiar_fondo_blur);


